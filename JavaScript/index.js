// aritmatika
/** + untuk opeasi penjumlahan.
- untuk operasi pengurangan.
* untuk operasi perkalian.
/ untuk operasi pembagian.
% untuk operasi sisa pembagian.
++ untuk operasi penambahan angka satu dari bilangan sebelumnya.
-- untuk operasi pengurangan angka satu dari bilangan sebelumnya.
 */

let a = 10
let b = 10
penjumlahan = a + b
pengurangan = a - b
perkalian = a * b
pembagian = a / b
modulus = a % b
increment = ++a
decrement = --b

console.log("Hasil Penjumlahan ="+penjumlahan)
console.log("Hasil Pengurangan ="+pengurangan)
console.log("Hasil perkalian ="+perkalian)
console.log("Hasil pembagian ="+pembagian)
console.log("Hasil modulus ="+modulus)
console.log("Hasil increment ="+ increment)
console.log("Hasil decrement ="+ decrement)