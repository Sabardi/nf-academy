// Menggunakan Metode splice():
// mengisi ulang array dalam JavaScript dengan berbagai cara, tergantung pada apa yang Anda inginkan untuk mencapai.

let myArray = [1,2,3,4,5];
myArray.splice(0, myArray.length, 'a','b','p') // menghapus data array nya mulai dari index 0 sampai panjangn index nya
// dan mengganti dengan data baru
console.log(myArray)

