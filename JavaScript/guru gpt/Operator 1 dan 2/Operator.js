let hasil1 = 5 + 3; // hasilnya 8
console.log("hasil nya = " + hasil1)

let hasil2 = 5 - 3; // hasilnya 2
console.log( "hasil nya = " +  hasil2)

let hasil3 = 5 * 3; // hasilnya 15
console.log( "hasil nya = " + hasil3)

let hasil4 = 15 / 3; // hasilnya 5
console.log( "hasil nya = " + hasil4)

// Modulus (%): Mengembalikan sisa dari pembagian dua bilangan.
let hasil5 = 5 % 3; // hasilnya 2
console.log( "hasil nya = " + hasil5)

// Increment (++): Menambahkan 1 ke nilai variabel.
let angka1 = 5;
angka1++; // sekarang angka adalah 6
console.log("++ = "+angka1)

// Decrement (--): Mengurangi 1 dari nilai variabel.
let angka2 = 5;
angka2--; // sekarang angka adalah 4
console.log("hasil nya = " + angka2)

// Exponentiation ( atau Math.pow())**: Menghitung hasil pangkat dari suatu nilai.
let hasil = 5 ** 3; // hasilnya 125
console.log("hasil = " + hasil)