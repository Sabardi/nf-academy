// Operator ternary, juga dikenal sebagai conditional operator, adalah operator yang memungkinkan untuk menulis kondisi dengan sintaks yang lebih singkat. Operator ini memiliki tiga operand: sebuah kondisi diikuti oleh dua ekspresi. Jika kondisi terpenuhi, ekspresi pertama dievaluasi; jika tidak, ekspresi kedua dievaluasi.
var age = 19;
var status = (age <=18) ? 'anak anak' : 'dewasa';
console.log(status); // Output: Dewasa
